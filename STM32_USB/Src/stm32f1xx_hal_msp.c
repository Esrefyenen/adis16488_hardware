void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim_base)
{
		/* Add Timter Interrupt Handler */
	struct ACC_DATA acc_data;
	struct GYRO_DATA gyro_data;
	uint8_t frame[20];
	
	frame[0] = 0xAA;
	frame[1] = 0xBB;
	
	frame[18] = 0xCC;
	frame[19] = 0xDD;
	
	acc_data = ReadAccData();
	gyro_data = ReadGyroData();
	
	
	frame[2] = imu_number &0xFF;;
	frame[3] = (imu_number >> 8) & 0xFF;
	frame[4] = (imu_number >> 16) & 0xFF;
	frame[5] = (imu_number >> 24) & 0xFF;
	
	frame[6] = acc_data.acc_x & 0xFF;
	frame[7] = acc_data.acc_x >> 8;
	frame[8] = acc_data.acc_y & 0xFF;
	frame[9] = acc_data.acc_y >> 8;
	frame[10] = acc_data.acc_z & 0xFF;
	frame[11] = acc_data.acc_z >> 8;
	frame[12] = gyro_data.gyro_x & 0xFF;
	frame[13] = gyro_data.gyro_x >> 8;
	frame[14] = gyro_data.gyro_y & 0xFF;
	frame[15] = gyro_data.gyro_y >> 8;
	frame[16] = gyro_data.gyro_z & 0xFF;
	frame[17] = gyro_data.gyro_z >> 8;
	
	imu_number++;
	
	/* data frame header*/
	CDC_Transmit_FS((uint8_t*)frame, 20);
	USBD_CDC_TransmitPacket(&hUsbDeviceFS);
	
	HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_15);
}
	

